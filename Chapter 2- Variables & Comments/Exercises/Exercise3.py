# Define the person's name with whitespace characters
person_name = "\t\t John Doe \n"

# Print the name with whitespace
print("Name with whitespace:", person_name)

# Print the name using each stripping function
print("Name using lstrip():", person_name.lstrip())
print("Name using rstrip():", person_name.rstrip())
print("Name using strip():", person_name.strip())
